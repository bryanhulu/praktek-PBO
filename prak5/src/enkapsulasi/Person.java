package enkapsulasi;

public class Person {
    private String name;
    private int age;
    private String alamat;

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for age
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age; 
    }

    public String getalamat() {
        return alamat;
    }
    public void setalamat(String alamat) {
        this.alamat = alamat;
    }

}
